m = int(input("Введите номер месяца: "))
# --------через dict----------------------
t = {'Зима': [12, 1, 2], 'Весна': [3, 4, 5], 'Лето': [6, 7, 8], 'Осень': [9, 10, 11]}
for item in t:
    if m in t[item]:
        print(item)
# --------через list----------------------
t = [12, 1, 2, 'Зима', 3, 4, 5, 'Весна', 6, 7, 8, 'Лето', 9, 10, 11, 'Осень']
for item in range(3, len(t), 4):
    if m == t[item - 3] or m == t[item - 2] or m == t[item - 1]:
        print(t[item])
